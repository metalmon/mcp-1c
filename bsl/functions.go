// searchEntry stores pre-lowered names and an index into BuiltinFunctions.
type searchEntry struct {
	idx      int
	nameLo   string
	nameEnLo string
}

var searchIndex []searchEntry

func init() {
	searchIndex = make([]searchEntry, len(BuiltinFunctions))
	for i, f := range BuiltinFunctions {
		searchIndex[i] = searchEntry{
			idx:      i,
			nameLo:   strings.ToLower(f.Name),
			nameEnLo: strings.ToLower(f.NameEn),
		}
	}
}

// Search finds functions by name (Russian or English), case-insensitive.
func Search(query string) []Function {
	queryLower := strings.ToLower(query)
	var results []Function
	for _, e := range searchIndex {
		if strings.Contains(e.nameLo, queryLower) || strings.Contains(e.nameEnLo, queryLower) {
			results = append(results, BuiltinFunctions[e.idx])
		}
	}
	return results
}
